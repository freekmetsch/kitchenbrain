// Bitmap — 12×12 pixel-art set, authored as ASCII grids ('#' = lit pixel) and
// compiled to fill paths at module load. Rendered on a 24-unit viewBox with
// 2-unit pixels so it scales like every other set. Butt caps + square geometry
// everywhere; the pot's steam is two 1-pixel-wide columns (Spinner contract:
// pot's LAST TWO paths are steam).

import type { IconDef, IconName, IconPathDef } from '../paths';

const PX = 2; // one grid cell = 2 viewBox units (12 cells → 24 units)

/** Compile an ASCII pixel grid into a single fill path (run-length per row). */
function px(rows: string[]): IconPathDef {
	let d = '';
	rows.forEach((row, y) => {
		let x = 0;
		while (x < row.length) {
			if (row[x] === '#') {
				let end = x;
				while (end < row.length && row[end] === '#') end++;
				d += `M${x * PX} ${y * PX}h${(end - x) * PX}v${PX}h${-(end - x) * PX}Z`;
				x = end;
			} else {
				x++;
			}
		}
	});
	return { d, fill: true };
}

